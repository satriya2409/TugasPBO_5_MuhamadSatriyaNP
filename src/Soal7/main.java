package Soal7;

public class main {
      public static void main(String[] args) {
        int i = 0;
        System.out.println("Menggunakan i++:");
        while (i < 5) {
            System.out.println(i++);
        }

        int j = 0;
        System.out.println("Menggunakan ++i:");
        while (j < 5) {
            System.out.println(++j);
        }
    }
}
